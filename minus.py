def subtract(a, b):
    """
    Subtracts the second number from the first number.
    
    Args:
        a: The first number
        b: The second number
        
    Returns:
        The result of a - b
    """
    return a - b


# Example usage
if __name__ == "__main__":
    num1 = 10
    num2 = 5
    result = subtract(num1, num2)
    print(f"{num1} - {num2} = {result}")
